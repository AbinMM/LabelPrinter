package com.smilesmile1973.label;

public class CellDimension {
	float width;
	float height;

	public CellDimension(float width, float height) {
		super();
		this.width = width;
		this.height = height;
	}

	public float getWidth() {
		return width;
	}

	public float getHeight() {
		return height;
	}
}
